export const getBgType = (type: string | undefined) => {
  switch (type) {
    case "bug":
      return "bg-type-bug";
    case "dark":
      return "bg-type-dark";
    case "dragon":
      return "bg-type-dragon";
    case "electric":
      return "bg-type-electric";
    case "fairy":
      return "bg-type-fairy";
    case "fighting":
      return "bg-type-fighting";
    case "fire":
      return "bg-type-fire";
    case "flying":
      return "bg-type-flying";
    case "ghost":
      return "bg-type-ghost";
    case "grass":
      return "bg-type-grass";
    case "ground":
      return "bg-type-ground";
    case "ice":
      return "bg-type-ice";
    case "poison":
      return "bg-type-poison";
    case "psychic":
      return "bg-type-psychic";
    case "rock":
      return "bg-type-rock";
    case "steel":
      return "bg-type-steel";
    case "water":
      return "bg-type-water";
    default:
      return "bg-type-normal";
  }
};

